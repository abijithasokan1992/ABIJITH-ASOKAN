-- 1. ROLES LOOKUP TABLE
CREATE TABLE roles (
    role_id VARCHAR2(30) PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    description VARCHAR2(500),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

